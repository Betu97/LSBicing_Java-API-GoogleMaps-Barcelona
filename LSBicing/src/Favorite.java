public class Favorite {
    private String nom;
    private String adreça;

    public Favorite(String nom, String adreça){
        this.nom = nom;
        this.adreça = adreça;
    }

    public String getNom(){
        return nom;
    }

    public String getAdreça(){
        return adreça;
    }
}
